package random;

public class String_equals {

	public static void main(String[] args) {

		char a='A';
		char b='A';
		if(a==b)
			System.out.println("true");
		else
			System.out.println("False");
		String A ="Hi";
		String B ="Hi";
		if(A.charAt(0)==B.charAt(0))
			System.out.println("true");
		else
			System.out.println("False");	
		if(A.equals(B))
			System.out.println("true");
		else
			System.out.println("False");
		if(A==B)
			System.out.println("true");
		else
			System.out.println("False");
		
	}
}
